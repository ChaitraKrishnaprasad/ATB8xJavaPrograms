package Java_Sept2024.ex_25092024_Loops;

public class Lab093_While_Loop1 {
    public static void main(String[] args) {
        //Whileloop
        //While A, B, C
        //A - initialization
        //B - Condition
        //C - increment/decrement

        int i = 0;// A
        while (i<10) { //B
            System.out.println(i);
            i++; //C

        }

    }
}
