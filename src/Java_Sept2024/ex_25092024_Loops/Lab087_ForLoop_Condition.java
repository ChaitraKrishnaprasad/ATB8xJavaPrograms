package Java_Sept2024.ex_25092024_Loops;

public class Lab087_ForLoop_Condition {
    public static void main(String[] args) {
        //Loop and condition together
        for (int i=1; i<=10; i++)
        {
            if (i==5){
                System.out.println("Five");
            }else{
                System.out.println(i);
            }
        }
    }
}
