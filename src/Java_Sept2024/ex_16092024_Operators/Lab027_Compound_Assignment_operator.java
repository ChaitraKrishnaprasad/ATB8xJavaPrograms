package Java_Sept2024.ex_16092024_Operators;

public class Lab027_Compound_Assignment_operator {
    public static void main(String[] args) {
        //Compound assignment operator
        int a = 10;
        // +=, -=, *=, /= (these are the compound operator)
        a += 20; // this statement means a = a+20
        System.out.println(a);
        a -= 20; // this statement means a = a-20
        System.out.println(a);
        a *= 20; // this statement means a = a*20
        System.out.println(a);
        a /= 10; // this statement means a = a/20
        System.out.println(a);
    }
}
