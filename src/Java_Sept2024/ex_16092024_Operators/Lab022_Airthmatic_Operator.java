package Java_Sept2024.ex_16092024_Operators;

public class Lab022_Airthmatic_Operator {
    public static void main(String[] args) {
        // Airthmatic Operator
        //+, _,*, / these airthmatic operator are Binary Operator
        int a = 10;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        //% - modulus -it gives remainder from the division sum
        System.out.println(a%b); // 10/2 = 5

    }
}
