package Java_Sept2024.ex_16092024_Operators;

public class Lab024_Unary_Operator {
    public static void main(String[] args) {
        // Unary Operator
        int a = +65; // + is a Unary Operator , when we print a Unary operator will print as 65 as by default + will be there
        int b = -1;// - is unary operator, all the -value has to mention the symbol -
        System.out.println(a);
        System.out.println(b);
        b =  b + 1;
        System.out.println(b);

    }
}
