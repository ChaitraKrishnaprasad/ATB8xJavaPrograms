package RevisionPlan_15days.Day4_Loop;

public class RLab12_While_Statement {
    public static void main(String[] args) {
        // Example 1: Basic while loop to print numbers 1 to 5
        System.out.println("Example 1: Counting from 1 to 5");
        int count = 1;
        while (count <= 5) {
            System.out.println("Count is: " + count);
            count++;
        }
    }
    }