package RevisionPlan_15days.Day4_Conditional_Loop;

public class RLab008_Wrapper_Class {
    public static void main(String[] args) {
        //create a primitive data type
        int num =10;
        Integer obj = num; //Autoboxing

        //Wrapper to Primitive || Unboxing

        Integer obj1 = new Integer(20);
        int num2 = obj1;

        System.out.println("Wraper Object : "+obj);
        System.out.println("Primitive Value : "+num2);




    }
}
