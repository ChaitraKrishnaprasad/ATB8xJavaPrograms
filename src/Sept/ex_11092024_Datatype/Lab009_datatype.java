package Sept.ex_11092024_Datatype;

public class Lab009_datatype {
    public static void main(String[] args) {
        char c = 'A';
        System.out.println(c);

        char c1 = '$';
        System.out.println(c1);

        char c2 = '\n';
        System.out.println(c2);

        char c3 = '\u1F6A';
        System.out.println(c3);
    }
}
