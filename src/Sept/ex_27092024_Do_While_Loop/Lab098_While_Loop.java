package Sept.ex_27092024_Do_While_Loop;

public class Lab098_While_Loop {
    public static void main(String[] args) {
        //  A B C - Intialization, condition and updation
        // while - when you are using for or while statement it will execute or verifies the condition before executing the block
        //do while - if the condition is false it will never execute the execution code
        // while - ICE - Intialize, Condition , Execution
        //Do while - Intialize, condition -only if it is true it will execute. false exit from the loop
        // for and while is called Entry Control Loop
        //
        int i = 0;
        while(i<10){
            System.out.println(i);
            i++;
        }
    }
}
