package Sept.ex_25092024_Loops;

public class Lab082_For_loop4 {
    public static void main(String[] args) {
        for(float f = 0.0f; f<=10.67; f++)
        {
            System.out.println("Hi, Used float data type "+ f);
        }

        for(byte s = 0; s <=10; s++)
        {
            System.out.println("Hi, Used byte data type "+ s);
        }

        for(long i = 0l; i <=10; i++)
        {
            System.out.println("Hi, Used long data type "+ i);
        }
    }
}
