package Sept.ex_18092024_TypeCasting;

public class Lab048_decrement_operator {
    public static void main(String[] args) {
        int a = 10;
        int result = a--;
        System.out.println(a);
        System.out.println(result);
        int result1 = --a;
        System.out.println(a);
        System.out.println(result1);
    }
}

