package Sept.DailyTasks;

public class Task1a_25092024 {
    public static void main(String[] args) {
        // Table of number n = 10, print table with  while.
        int n = 10;
        int i = 1;
       while(i<=10){
            System.out.println(n + " x " + i + " = " + (n * i));
            i++;
        }
    }
}
