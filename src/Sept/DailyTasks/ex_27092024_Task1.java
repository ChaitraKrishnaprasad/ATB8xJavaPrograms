package Sept.DailyTasks;

public class ex_27092024_Task1 {
    public static void main(String[] args) {
        //Give me example of while vs do while loop in Lab ex.
// While Statement
        int i =0;
        while(i<10){
            System.out.println("example of While statement = "+ i);
            i++;
        }

        //do while statement
        int n=10;
        do{
            System.out.println("example of do-while statement = " + n);
            n--;
        } while(n>=1);
    }
}
