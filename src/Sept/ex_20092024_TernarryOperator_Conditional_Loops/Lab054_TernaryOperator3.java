package Sept.ex_20092024_TernarryOperator_Conditional_Loops;

public class Lab054_TernaryOperator3 {
    public static void main(String[] args) {
        // Max, Min between two number
        int tushar_salary = 4;
        int prabhu_salary = 7;
        int max = tushar_salary > prabhu_salary ? tushar_salary : prabhu_salary;
        System.out.println(max);
    }
}
